# Agora vamos calcular a média de três notas fornecidas na entrada do usuário

# Solicitar as notas do usuário
nota1 = float(input("Por favor, insira a primeira nota: "))
nota2 = float(input("Por favor, insira a segunda nota: "))
nota3 = float(input("Por favor, insira a terceira nota: "))

# Calcular a média das notas
media = (nota1 + nota2 + nota3) / 3

# Exibir o resultado
print("A média das três notas é:", media)
